
public class Calculator {

    private int value;

    public Calculator() {
        this.value = value;
    }

    public int getValue() {
        return this.value;
    }

    public void Plus(int n) {

        this.value = this.value + n;
    }

    public void Minus(int n) {

        this.value = this.value - n;
    }

    public void Reset() {

        this.value = 0;
    }
}
